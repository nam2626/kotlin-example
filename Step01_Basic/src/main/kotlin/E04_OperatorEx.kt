fun main(){
    //수식 작성 방법
    var a = 10
    var b = 5
    var result1 = a + b
    /*
    *   수식 작성 원리
    *       수학에서 아래 식을 보면
    *       y = x + 1
    *       x = 10이라면 y = 10 + 1 = 11이 된다는 것을 알 수 있다.
    *       하지만 코드에서는 조금 의미가 다르다.
    *       y = x + 1 이라는 식이 코드에서는 y에 x + 1의 결과를 대입(저장)하는 것이다.
    *
    *       이와 같은 원리로 result1 = a + b는 a + b의 결과를 result1에 저장하는 것이다.
    * */
    println(result1)
}